///...Funcion de suma...///
function suma (a, b){
    return a + b
}
const resultado_suma = suma(2, 8)
console.log("La suma de  2 + 8 es:",resultado_suma)

///...Funcion de resta...///
const resta = function(a, b){
    return a - b
}
const resultado_resta = resta(5, 7)
console.log("La resta de 5 - 7 es:",resultado_resta)

///... Funcion de Producto...///
const producto = (a, b) => a * b
const resultado_producto = producto(6, 2)
console.log("El producto de 6 * 2 es:",resultado_producto)

///...Funcion de Dividir...///
const dividir = (a, b) => a / b
